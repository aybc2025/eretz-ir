// פונקציית בדיקה
export function checkAnswer(letter, category, answer) {
  if (!answer || answer.trim() === "") return { valid: false, known: false };

  const normalized = answer.trim().toLowerCase();
  const categoryWords = words[letter]?.[category] ?? [];

  for (const variants of categoryWords) {
    for (const variant of variants) {
      if (variant.toLowerCase() === normalized) {
        return { valid: true, known: true, canonical: variants[0] };
      }
    }
  }

  // בדיקה שהמילה לפחות מתחילה באות הנכונה
  const firstChar = answer.trim()[0];
  if (firstChar === letter) {
    return { valid: true, known: false };
  }

  return { valid: false, known: false };
}

// מחזיר תשובה נכונה אקראית עבור אות וקטגוריה
export function getRandomCorrectAnswer(letter, category) {
  const categoryWords = words[letter]?.[category] ?? [];
  if (categoryWords.length === 0) return null;
  const pick = categoryWords[Math.floor(Math.random() * categoryWords.length)];
  return pick[0]; // המילה הראשונה = הצגה למשתמש
}

